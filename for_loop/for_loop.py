##
##for loop from 0 to n
for i in range(10) :
	print("current i = ", i)
##
##for loop from a to b
for i in range(-5,5) :
	print("current i = ", i)
	
##
##for loop for each character in a string
aString = "Hello World"
for c in aString :
	print("current character = ", c)

##
##for loop for each items in a collection
fruits = ["apple","banana","strawberry"]
for fruit in fruits :
	print("current fruit in fruits = ", fruit)
	
